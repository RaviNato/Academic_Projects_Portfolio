print("#######  Programa para calcular perimetro de retangulos.   ########");


lado1 = float(input("Quanto mede, em centimetros, a altura? "));
lado2 = float(input("Quanto mede, em centimetros, a largura? "));


perimetro = ((lado1*2)+(lado2*2));

print("O perimetro vale", perimetro, "cm");


